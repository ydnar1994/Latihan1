package javaapplication3;

public class Aritmatika {
    public static Integer pertambahan(Integer bil1, Integer bil2){
        Integer Hasil=0;

        Hasil = bil1+bil2;

        return Hasil;
    }

    public static Integer pengurangan(Integer bil1,Integer bil2){
        Integer Hasil=0;

        Hasil = bil1-bil2;

        return Hasil;
    }

    public static void perkalian(Integer bil1,Integer bil2){
        Integer Hasil=0;

        Hasil = bil1*bil2;

        System.out.println(Hasil);
    }

    public static Integer pembagian(Integer bil1,Integer bil2){
        Integer Hasil=0;

        Hasil = bil1/bil2;

        return Hasil;
    }
}
